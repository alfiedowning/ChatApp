import Foundation
import SwiftUI

extension Font {
    
    static var bodyText: Font {
        return Font.custom("Montserrat-Regular", size: 16)
    }
    
    static var largeHeading: Font {
        return Font.custom("Montserrat-Light", size: 24)
    }
    
    static var smallHeading: Font {
        return Font.custom("Montserrat-Medium", size: 20)
    }
    
    static var subheading: Font {
        return Font.custom("Montserrat-Medium", size: 16)
    }
    
    static var majorHeading: Font {
        return Font.custom("Montserrat-Bold", size: 30)
    }
    
    static var italic: Font {
        return Font.custom("Montserrat-Italic", size: 14)
    }
    
    
    
    
    
    
}
